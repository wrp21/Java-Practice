package JumpToJava;

public class Operator {
  public static void main(String[] args) {
    System.out.println(1 + 1);
    System.out.println(1 - 1);
    System.out.println(2 * 2);
    System.out.println(4 / 2);
    System.out.println(5.0 / 3);
    System.out.println(5F / 3);
    System.out.println(5 / 3);
    System.out.println(5 % 3);
  }
}
